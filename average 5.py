#Given 5 numbers A, B, C, D, E as input. Print the average of these 5 numbers. 

A = float(input("Enter A: "))
B = float(input("Enter B: "))
C = float(input("Enter C: "))
D = float(input("Enter D: "))
E = float(input("Enter E: "))

average = (A + B + C + D + E) / 5

print("Average:", average)
